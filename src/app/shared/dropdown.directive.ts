import { Directive, ElementRef, HostBinding, HostListener } from "@angular/core";

@Directive({
    selector: '[appDropdown]'
})
export class DropdownDirective {
    @HostBinding('class.open') isOpen = false; //Controls the dropdown feature of menus.  False = closed, True = Open
    @HostListener('document:click', ['$event']) toggleOpen(event: Event) {
        this.isOpen = this.elRef.nativeElement.contains(event.target) ? !this.isOpen : false; //This makes mouse-clicks anywhere on the document close the menu.
    }
    constructor(private elRef: ElementRef) {}
}